def add(num1, num2):
    '''When: when you want to add two numbers
    Why: to add two numbers
    '''
    return num1 + num2

def subtract(num1, num2):
    return add(num1, num2 * -1)

def multiply(num1, num2):
    return num1 * num2

def divide(num1, num2):
    return num1 / num2

def square(num1):
    return num1 ** 2

def cube(num1):
    return num1 ** 3

def power(num1, num2):
    return num1 ** num2

def mod(num1, num2):
    return num1 % num2
