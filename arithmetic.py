def add(num1, num2):
    '''When: when you want to add two numbers
    Why: to add two numbers
    '''
    return num1 + num2

def subtract(num1, num2):
    '''When: you want to subtract two numbers
    Why: when you want to subtract two numbers'''
    return add(num1, num2 * -1)

def multiply(num1, num2):
    '''When: you want to multiply two numbers
    Why: when you want to multiply two numbers'''
    return num1 * num2

def divide(num1, num2):
    '''When: you want to divide two numbers
    Why: when you want to divide two numbers'''
    return num1 / num2

def square(num1):
    '''When: you want to multiply a number by itself
    Why: when you want to multiply a number by itself'''
    return num1 ** 2

def cube(num1):
    '''When: you want to cube a number
    Why: when you want to cube a number'''
    return num1 ** 3

def power(num1, num2):
    '''When: you want to raise a number to another number
    Why: when you want to raise a number to another number'''
    return num1 ** num2

